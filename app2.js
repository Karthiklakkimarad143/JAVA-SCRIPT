//differtiate b/w  var, let, const--------

// var keyword---------

var a; // it allows declearation
console.log(a);
a=10; // it allows initilization
console.log(a);
a="dinga"; // it allows re-initilization
console.log(a);
var a="jspider"; // it allows re-declearation
console.log(a)

// let keyword----------

let a; // it allows declearation
console.log(a);
a=20; // it allows initilization
console.log(a);
a="dinga" // it allows re-initilization
console.log(a);
let a="jspider" // re-declearation is not possible
console.log(a);

// const keyword-------

const a; // declearation is not possible
console.log(a);
const a=20; // it allows declearation and intilization
console.log(a);
a="dinga"; // re-initilization is not possible
console.log(a);
const a="jspider"; // re-declearation is not possible
console.log(a);







